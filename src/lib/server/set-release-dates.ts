/**
 * Get the release date for a set code
 * @param setCode - The set code (case-insensitive)
 * @returns The release date string (YYYY-MM-DD) or undefined if not found
 */
export function getSetReleaseDate(setCode: string): string | undefined {
  return SET_RELEASE_DATES[setCode.toUpperCase()]
}

/**
 * Compare two sets by release date (for sorting)
 * Newer sets come first (descending order)
 * @returns negative if a is newer, positive if b is newer, 0 if same/unknown
 */
export function compareSetsByReleaseDate(setCodeA: string, setCodeB: string): number {
  const dateA = getSetReleaseDate(setCodeA)
  const dateB = getSetReleaseDate(setCodeB)

  // If both dates are unknown, fall back to alphabetical
  if (!dateA && !dateB) {
    return setCodeA.localeCompare(setCodeB)
  }

  // Unknown dates sort last
  if (!dateA) return 1
  if (!dateB) return -1

  // Compare dates (newer first = descending order)
  return dateB.localeCompare(dateA)
}

/**
 * Compare two sets by release date for ascending order (older first)
 */
export function compareSetsByReleaseDateAsc(setCodeA: string, setCodeB: string): number {
  const dateA = getSetReleaseDate(setCodeA)
  const dateB = getSetReleaseDate(setCodeB)

  // If both dates are unknown, fall back to alphabetical
  if (!dateA && !dateB) {
    return setCodeA.localeCompare(setCodeB)
  }

  // Unknown dates sort last
  if (!dateA) return 1
  if (!dateB) return -1

  // Compare dates (older first = ascending order)
  return dateA.localeCompare(dateB)
}
